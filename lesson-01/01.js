// С помощью цикла while найдите сумму чисел от 1 до 20
// Результат присвойте переменной sum

// your code


let sum = 0;

let add = 1;

while (add <= 20) {

    // console.log(`Текущая добавка =  ${add}`);

    sum = sum + add;

    // console.log(sum);

    add= add + 1;
    
}


console.log(`Текущая сумма чисел =  ${sum}`);